<h3>
Global Mind Share is an International Non-Profit Organization to connect our minds.
</h3>

<h4>
Together we are building a global cognitive network that aggregates the 
problems faced by and solutions suggested by mankind. This is a tool to maximize 
the collective thought power of our planet to improve future prosperity and 
happiness of all people. 
</h4>

<h4>
All the world's people want a better future for themselves, their children, 
their grandchildren, and all future generations. Global Mind Share will collect 
the problems that are most important to the world, the problems that must be 
solved if mankind is going to survive and endure. Then, we will solve them together. 
Once solutions are suggested and rated, the most popular solutions are passed on to international 
decision-making entities. 
</h4>

<h4>
Join us as anonymous participants, members, or volunteers.
</h4>

<h4>
